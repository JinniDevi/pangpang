package kr.or.ddit.vo;

public class ReviewVO {
	private int  re_no;
	private String  re_content;
	private String  re_date;
	private int  re_rate;
	private String  re_category;
	private int  pay_no;
	
	public int getRe_no() {
		return re_no;
	}
	public void setRe_no(int re_no) {
		this.re_no = re_no;
	}
	public String getRe_content() {
		return re_content;
	}
	public void setRe_content(String re_content) {
		this.re_content = re_content;
	}
	public String getRe_date() {
		return re_date;
	}
	public void setRe_date(String re_date) {
		this.re_date = re_date;
	}
	public int getRe_rate() {
		return re_rate;
	}
	public void setRe_rate(int re_rate) {
		this.re_rate = re_rate;
	}
	public String getRe_category() {
		return re_category;
	}
	public void setRe_category(String re_category) {
		this.re_category = re_category;
	}
	public int getPay_no() {
		return pay_no;
	}
	public void setPay_no(int pay_no) {
		this.pay_no = pay_no;
	}
	
	
}
